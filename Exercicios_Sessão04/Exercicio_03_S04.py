# Exercicio 03: Peça ao usúario para digitar três valores inteiros e imprima a soma deles.

primeiro_numero = int(input('Digite o primeiro número inteiro:'))
segundo_numero = int(input('Digite o Segundo número inteiro:'))
terceiro_numero = int(input('Digite o Terceiro número inteiro:'))
soma = primeiro_numero + segundo_numero + terceiro_numero

print(f'A soma de {primeiro_numero} + {segundo_numero} + {terceiro_numero} é {soma}')
